package arithmetic;

public class Operators {
	public static void main(String args[])
	{
		
		int num1=100;
		int num2=25;
		System.out.println("Output of sum :" + (num1 + num2) );
		System.out.println("Output of substraction : " + (num1 - num2) );
		System.out.println("Output of substraction : " + (num1 * num2) );
		System.out.println("Output of substraction : " + (num1 / num2) );
	}
	

}
